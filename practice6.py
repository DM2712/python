age = int(input("Enter your age:"))

if age<=5 :
    print("You can travel without paying")
elif age>5 and age<=12 :
    print("Your charge for the Travel is 50/- RS")
elif age>12 and age<=60 :
    print("Your charge for the Travel is 100/- RS")
else :
    print("Your charge for the Travel is 40/- RS")
    
